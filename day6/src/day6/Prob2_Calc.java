public class Prob2_Calc {

    double add(double a,double b){
        return a + b;
    }
    
    double sub(double a,double b){
        return a - b;
    }
    
    double mul(double a, double b ) {
    	return a * b;
    }
    
    double div(double a, double b) {
    	return a / b;
    }
}
